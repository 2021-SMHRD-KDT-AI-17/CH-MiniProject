package JDBC_MODEL;

public class EndingDTO {

	// 결말 DTO
	

	// 변수
	private String end; // 엔딩 결과
	private int ch; // 매력
	private int hp; // 채력
	private int code; // 코딩 능력
	private int stree; //스트레스
	private int lot;
	
	// getter/ setter
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
	public int getCh() {
		return ch;
	}
	public void setCh(int ch) {
		this.ch = ch;
	}
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public int getStree() {
		return stree;
	}
	public void setStree(int stree) {
		this.stree = stree;
	}
	public int getlot() {
		return lot;
	}
	public void setlot(int lot) {
		this.lot = lot;
	}

	
	
}
