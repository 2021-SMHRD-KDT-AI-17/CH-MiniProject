package JDBC_MODEL;

public class UserInfoDTO {

	// 회원 정보 DTO
	
	// 전역 변수
	private String id;
	private String pw;
	
	// getter/setter
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	
}
